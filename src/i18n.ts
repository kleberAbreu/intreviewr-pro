export function modelNote(lang: UiLanguage, provider: Provider, id: string, fallback?: string): string | undefined {
  if (lang === 'en-US') return englishModelNotes[`${provider}:${id}`] ?? fallback
  return fallback
}

export function formatAppError(error: unknown, lang: UiLanguage, fallback: string): string {
  const codedErrors: Record<string, string> = {
    RESEARCH_BRIEF_FAILED: appCopy[lang].errors.researchFailed,
    INTERVIEW_PLAN_FAILED: appCopy[lang].errors.planFailed,
    REPORT_INVALID: appCopy[lang].errors.reportInvalid,
    MODEL_EMPTY_RESPONSE: appCopy[lang].errors.modelEmpty,
    MODEL_JSON_NOT_FOUND: appCopy[lang].errors.modelJsonNotFound,
    SESSION_STATE_LOST: appCopy[lang].errors.lostState,
  }
  if (error instanceof Error && error.message.startsWith('MISSING_API_KEY:')) {
    return appCopy[lang].errors.missingKey(error.message.replace('MISSING_API_KEY:', ''))
  }
  if (error instanceof Error && codedErrors[error.message]) return codedErrors[error.message]
  if (typeof error === 'string' && codedErrors[error]) return codedErrors[error]
  if (error instanceof Error && error.message) return sanitizeErrorMessage(error.message)
  if (typeof error === 'string' && error) return sanitizeErrorMessage(error)
  return fallback
}
